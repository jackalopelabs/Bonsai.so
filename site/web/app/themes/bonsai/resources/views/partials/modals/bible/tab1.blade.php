        <h3>Bonsai.so - <span class="mute">Patreon for Web3</span></h3>

        <h3 class="badge badge-secondary badge-lg mt-3"><strong>The Problem:</strong> Centralized  Closed Platforms</h3>
        <p>Whether it’s Patreon, OnlyFans, Teachable, Shopify, etc– they own your content, your audience, your business, and so they basically own you.</p>

        <h3 class="badge badge-primary badge-lg mt-3"><strong>The Solution:</strong> Decentralized Open-Source Platforms</h3>
        <p>In the future, you will have a convenient open-source option. You will have the option of owning the software that you and your community use. You will own all of the rights to make the decision whether you want to run it on your own server, or pay somebody else to manage it for you. Either way, you own the full code repository and the database. You will own your business.</p>

        <h3 class="badge badge-dark badge-lg mt-3">Key Differentiators</h3>
        <ul>
          <li>Unlike Creaton, Bonsai gives you the option to pay in cash or other trades of value</li>
          <li>Unlike Patreon, Bonsai is open-source and web3 ready out of the box</li>
        </ul>

        <h3 class="badge badge-dark badge-lg mt-3">Education</h3>
        <p>For now, I think going to focus on education as the foundation of the product. I know that you want Unlock Protocol to widely adopted, and I think more education on the topic will help. Especially in video form.</p>

        <h3 class="badge badge-dark badge-lg mt-3">Bonsai DAO</h3>
        <p>I’m glad that I haven’t taken any investments yet, because a company like this needs to be formed as a DAO. Most traditional investors wouldn’t have allowed that. I would love to learn more about how you’ve structured Unlock DAO.</p>

        <h3 class="badge badge-dark badge-lg mt-3">Focus</h3>
        <p>I’m going to focus on Patreon for now. I wasn’t focused enough in 2021. In 2022 I’m going to focus on a narrow vertical, rather than building a wide array of features. I will only add features when/if one of our grants receives interest, and I can find a good team to complete it.</p>

        <h3 class="badge badge-dark badge-lg mt-3">Simple LMS</h3>
        <p>Patreon can be used as an LMS. But when people pay for content, I see little need in quizzing them. You give them the content they pay for, and if they value it, they will use it. That allows us to cut out most of the LMS functionality, and still use it as an LMS.</p>

        <h3 class="badge badge-dark badge-lg mt-3">Freemium Content</h3>
        <p>Julien Genestoux from Unlock Protocol told me about a plan to allow people to unlock content with a Twitter follow or share. Also a Patreon OAuth integration. I see this as more of an OnlyFans feature where you can follow for free, then pay per individual post. Except instead of paying for individual posts, you could incentivize a visitor to unlock it with a subscribe, follow, share, comment, etc. It’s almost like a freemium version of Patreon.</p>

        <p>Later on, I could build a custom way to do this with Stripe, but I will focus on using Unlock Protocol for now.</p>

