        <small class="mute uppercase mr-3">10</small>
        <span class="mr-4">Bonsai Manifesto</span>
      </a>
      <ul class="inner mt-3" style="">
        <h3>Standard Operating Principles</h3>
        <div class="badge badge-primary">We Scratch Our Own Itch</div>
        <p>Bonsai at the core, is ultimately THE platform we want to run your community.</p>

        <div class="badge badge-primary">We Are Open Source</div>
        <p>There will always be an open source version of Bonsai, which I teach students how to make. It will teach students how to build an MVP. This portion of the company is called The Bonsai Foundation.</p>

        <div class="badge badge-primary">We Go All-In on Design</div>
        <p>I have a belief that the best design will ultimately win out. But that's just a belief.</p>
        <p>In reality, the first to market usually wins, because they win free real estate in the customer's mind. Being first is better than having a better design in the short term.</p>
        <p>So we will focus on being first in our category, but only temporarily until we focus on design innovation– which is our ultimate goal.</p>

        <div class="badge badge-primary">Monetization</div>
        <p><i>Enforcable by law for all time and across all universes:</i></p>
        <p>No Data sharing of user data on Bonsai.so</p>
        <p>Bonsai will be community supported, and we will handle our user data responsible. Since Bonsai is open-source, we can't control what other developers do with their user data.</p>
